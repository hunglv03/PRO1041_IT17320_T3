    public String getGioiTinh(int gioiTinh) {
        if (gioiTinh == 1) {
            return "Nam";
        } else if (gioiTinh == 2) {
            return "Nữ";
        } else {
            return null;
        }
    }

    public String getTrangThai(int trangThai) {
        if (trangThai == 1) {
            return "Khách Hàng Mới";
        } else if (trangThai == 2) {
            return "Khách Lâu Năm";
        } else {
            return null;
        }
    }

    public void rdGioiTinh() {
        buttonGroupg.add(rdNam);
        buttonGroupg.add(rdNu);
    }

    public void rdTrangThai() {
        buttonGroupt.add(rdKhachHangMoi);
        buttonGroupt.add(rdKhachLauNam);
    }

    public void getData() {
        DefaultTableModel dtm = (DefaultTableModel) this.tbKH.getModel();
        dtm.setRowCount(0);
        for (KhachHangVM kh : this.khService.getall()) {
            Object[] rowData = {
                kh.getId(), kh.getMaKH(), kh.getTenKH(), getGioiTinh(kh.getGioiTinh()), kh.getSdt(), kh.getDiaChi(), getTrangThai(kh.getTrangThai())
            };
            dtm.addRow(rowData);
        }
    }

