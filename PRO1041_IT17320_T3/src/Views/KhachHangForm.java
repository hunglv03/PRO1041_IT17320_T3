    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        KhachHang kh = this.getLoadData();
        if (kh == null) {
            return;
        }
        if (rdNam.isSelected()) {
            kh.setGioiTinh(1);
        } else if (rdNu.isSelected()) {
            kh.setGioiTinh(2);
        }

        if (rdKhachHangMoi.isSelected()) {
            kh.setTrangThai(1);
        } else if (rdKhachLauNam.isSelected()) {
            kh.setTrangThai(2);
        }
        this.khService.insert(kh);
        this.getData();
        this.clear();
        JOptionPane.showMessageDialog(this, "Thêm thành công");
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        int row = tbKH.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Hãy chọn dòng cần sửa");
        }
        KhachHang kh = this.getLoadData();
        if (kh == null) {
            return;
        }
        if (rdNam.isSelected()) {
            kh.setGioiTinh(1);
        } else if (rdNu.isSelected()) {
            kh.setGioiTinh(2);
        }

        if (rdKhachHangMoi.isSelected()) {
            kh.setTrangThai(1);
        } else if (rdKhachLauNam.isSelected()) {
            kh.setTrangThai(2);
        }
        this.khService.update(kh, kh.getId());
        this.getData();
        this.clear();
        JOptionPane.showMessageDialog(this, "Sửa thành công");
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int row = tbKH.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Hãy chọn dòng cần xóa");
        } else {
            int confirm = JOptionPane.showConfirmDialog(this, "Xác nhận xóa??");
            if (confirm == JOptionPane.YES_NO_OPTION) {
                String id = tbKH.getValueAt(row, 0).toString();
                this.khService.delete(id);
                this.getData();
                this.clear();
            }
        }
        JOptionPane.showMessageDialog(this, "Xóa thành công");
    }//GEN-LAST:event_btnXoaActionPerformed

    private void tbKHMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbKHMouseClicked
        // TODO add your handling code here:
        int row = tbKH.getSelectedRow();
        lbId.setText(tbKH.getValueAt(row, 0).toString());
        txtMakh.setText(tbKH.getValueAt(row, 1).toString());
        txtTenkh.setText(tbKH.getValueAt(row, 2).toString());

        if (tbKH.getValueAt(row, 3).equals("Nam")) {
            rdNam.setSelected(true);
        } else {
            rdNu.setSelected(true);
        }
        txtdc.setText(tbKH.getValueAt(row, 5).toString());
        txtSdt.setText(tbKH.getValueAt(row, 4).toString());
        if (tbKH.getValueAt(row, 6).equals("Khách Hàng Mới")) {
            rdKhachHangMoi.setSelected(true);
        } else {
            rdKhachLauNam.setSelected(true);
        }
    }//GEN-LAST:event_tbKHMouseClicked

    /**
     * @param args the command line arguments
     */
    private KhachHang getLoadData() {
        String id = this.lbId.getText();
        String ma = this.txtMakh.getText().trim();
        String ten = this.txtTenkh.getText().trim();
        String dc = this.txtdc.getText().trim();
        String sdt = this.txtSdt.getText().trim();
        if (ma.equals("") || ten.equals("") || dc.equals("") || sdt.equals("")) {
            JOptionPane.showMessageDialog(this, "Không được để trống!!!");
            return null;
        }
        KhachHang kh = new KhachHang(id, ma, ten, WIDTH, sdt, dc, WIDTH);
        return kh;
    }

    private void clear() {
        lbId.setText("");
        txtMakh.setText("");
        txtTenkh.setText("");
        buttonGroupg.clearSelection();
        txtdc.setText("");
        txtSdt.setText("");
        buttonGroupt.clearSelection();
    }

